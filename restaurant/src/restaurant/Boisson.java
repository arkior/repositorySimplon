package restaurant;

public class Boisson
{
	private final String  EAU_PLATE ="EAU PLATE";
	private final String  EAU_GAZEUSE ="EAU GAZEUSE";
	private final String  SODA ="SODA";
	
	//constructeur
	public Boisson() {}
	
	
	public String getEauPlate()
	{
		return this.EAU_PLATE;
	}
	
	public String getEauGazeuse()
	{
		return this.EAU_GAZEUSE;
	}
	
	public String getSoda()
	{
		return this.SODA;
	}

}
