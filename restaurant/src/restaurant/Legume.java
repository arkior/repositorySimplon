package restaurant;

public class Legume
{
	
	private final String  LEGUMES_FRAIS ="LEGUMES-FRAIS";
	private final String  RIZ ="RIZ";
	private final String  FRITES ="FRITES";
			

	
	public Legume() {}
	
	
	public String getLegumesFrais()
	{
		return this.LEGUMES_FRAIS;
	}
	
	public String getFrites()
	{
		return this.FRITES;
	}
	
	public String getRiz()
	{
		return this.RIZ;
	}

}
